package javapractice;

public class TypeCasting {
	public static void main(String[] args) {
		//widening
		byte a=56;
		int b;
		//here we are assigning the value of a(byte) to b(int)
		//converting the one data type to another data type is known as type casting
		b=a;
		System.out.println(b);
	}

}
